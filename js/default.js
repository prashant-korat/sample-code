function ajaxLoad_json(url, postData, getVal)
{
	var getData = '';
	$.ajax({
		type: "POST",
		url: url,
		data: postData, //{key1: 'value1', key2: 'value2'}
//		dataType: "json",
		async:false,
		success: function(html) {
			if(getVal)
			  getData = html; 
			 
		}
	});
	return getData;
}

